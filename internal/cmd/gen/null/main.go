//nolint:forbidigo
package main

import (
	_ "embed"
	"fmt"
	"go/format"
	"os"
	"strings"

	"github.com/bangumi/server/internal/pkg/generic/slice"
)

//go:embed template/template.go
var tpl string

const generatedHeader = "// Code generated by ./internal/cmd/gen/null/main.go. DO NOT EDIT.\n\n"

const fileAllTypes = `package null
func AllTypes() []interface{}{
	return []interface{}{
		%s
	}
}
`

func main() {
	var types = []string{
		"bool", "string", "float32", "float64",
		"uint8", "uint16", "uint32", "uint64", "uint",
		"int8", "int16", "int32", "int64", "int",
	}

	for _, s := range types {
		var code = tpl
		code = strings.ReplaceAll(code, "convertToDriverValue(t.Type)", valueConverter(s))
		code = strings.ReplaceAll(code, "GenericType", s)
		code = strings.ReplaceAll(code, "Type", destType(s))

		outPath := fmt.Sprintf("./internal/pkg/null/%s.gen.go", s)
		formatted, err := format.Source([]byte(generatedHeader + code))
		if err != nil {
			panic(err)
		}

		err = os.WriteFile(outPath, formatted, 0600) //nolint:gomnd
		if err != nil {
			panic(err)
		}
	}

	var a = strings.Join(slice.Map(types, func(s string) string {
		return destType(s) + "{},"
	}), " ")

	outPath := "./internal/pkg/null/types.gen.go"
	formatted, err := format.Source([]byte(generatedHeader + fmt.Sprintf(fileAllTypes, a)))
	if err != nil {
		panic(err)
	}

	err = os.WriteFile(outPath, formatted, 0600) //nolint:gomnd
	if err != nil {
		panic(err)
	}
}

func valueConverter(t string) string {
	switch t {
	case "uint8", "uint16", "uint32", "uint64", "int8", "int16", "int32", "int64":
		return "int64(t.Type)"
	case "bool", "string":
		return "t.Type"
	}

	return "tt.Type"
}

func destType(s string) string {
	return strings.ToUpper(s[:1]) + strings.ToLower(s[1:])
}
